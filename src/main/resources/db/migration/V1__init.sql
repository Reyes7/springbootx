CREATE TABLE USER (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY,
  first_name VARCHAR(30),
  last_name  VARCHAR(50),
  login      VARCHAR(20),
  oldPassword   VARCHAR(60),

  PRIMARY KEY(id)
);

CREATE TABLE TASK (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY,
  task_name VARCHAR(30),
  done      BOOLEAN,
  user_id   INTEGER,

  FOREIGN KEY(user_id) REFERENCES USER(id)
);